class MyRecord(AggregateRecord):
    a: Signal = Wire(UInt(8))
    b: Signal = Wire(UInt(4))


class RecordWithArray(AggregateRecord):
    a: Signal = Wire(UInt(8))
    vec: Array = Array([Wire(UInt(2)) for _ in range(3)])  # 3×2 bits


